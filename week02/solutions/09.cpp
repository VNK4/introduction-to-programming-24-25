#include <iostream>

int main() {
    int n = 0, t = 0;

    std::cin >> n >> t;

    // Най-оптимално е всеки път да увеличаваме n с 1 и да намаляме m с 1,
    // но това е същото като всеки път да увеличаваме n с 2. Имаме t на брой 
    // операции и следователно е нужно да намерим n + 2 * t.

    int m = n + 2 * t;

    std::cout << m;
}